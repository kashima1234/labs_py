from math import sqrt, fabs, e                                #Используем библиотеку math
print('Введите координаты первой вершины треугольника: ') #Выводим координаты вершин треугольника
x1, y1 = map(int, input().split())
print('Введите координаты второй вершины треугольника: ')
x2, y2 = map(int, input().split())
print('Введите координаты третьей вершины треугольника: ')
x3, y3 = map(int, input().split())
AB = sqrt((x2-x1)**2+(y2-y1)**2)                                         #Считаем длину АБ
BC= sqrt((x3-x2)**2+(y3-y2)**2)                                          #Считаем длину BC                           
AC = sqrt((x3-x1)**2+(y3-y1)**2)                                         #Считаем длину AC
if fabs(AB+BC-AC) <= 1e-8 or fabs(AB+AC-BC) <= 1e-8 or fabs(BC+AC-AB) <= 1e-8:  #1e-8==10**-8 
    print ('Это не треугольник!')
    
else:
    print('Длина стороны AB= {:.7}'.format(AB))            #Печатаем длину стороны AB
    print('Длина стороны BC= {:.7}'.format(BC))            #Печатаем длину стороны BC
    print('Длина стороны AC= {:.7}'.format(AC))            #Печатаем длину стороны AC
    if fabs(AB - AC) <= 1e-8 or fabs(AB - BC) <= 1e-8 or fabs(AC - BC) <= 1e-8:
        print('Заданный треугольник является равнобедренным треугольником.')
    else:
        print('Заданный треугольник не является равнобедренным треугольником.')
    mediana = 0
    if BC <= AB and BC <= AC:
        mediana = sqrt((2*(AB*AB+AC*AC) - BC*BC)/4)        #Ишем медиану от наименьшего угла
    elif AC <= AB and AC <= BC:
        mediana = sqrt((2*(AB*AB+BC*BC) - AC*AC)/4)
    elif AB <= AC and AB <= BC:
        mediana = sqrt((2*(AC*AC+BC*BC) - AB*AB)/4)
    print('Медиана из наименьшего угла треугольника= {:.7}'.format(mediana)) #Печатаем медиану из наименьшего угла треугольника
    print('Введите координаты произвльной точки треугольника:')   #Выводим координаты произвольной точки
