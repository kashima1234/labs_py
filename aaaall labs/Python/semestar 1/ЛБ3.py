    s1 = 1/2*abs((x-x1)*(y2-y1)-(y-y1)*(x2-x1))   #Вычисляем площад треугольника ABD
    s2 = 1/2*abs((x-x3)*(y2-y3)-(y-y3)*(x2-x3))   #Вычисляем площад треугольника BCD
    s3 = 1/2*abs((x-x1)*(y3-y1)-(y-y1)*(x3-x1))   #Вычисляем площад треугольника ACD
    if fabs(s - s1 - s2 - s3) > 1e-8:                  #Ишем принадлежит ли введенная произвольная точка в треугольнике или нет
        print('Точка S не находится внутри треугольника.')   
    else:
        print ('Точка S  находится внутри треугольника.')
        DAB = 2*s1 / AB                  # Вычисляем расстояние от точки до АВ
        DBC = 2*s2 / BC                  # Вычисляем расстояние от точки до ВС
        DAC = 2*s3 / AC                  # Вычисляем расстояние от точки до АС
